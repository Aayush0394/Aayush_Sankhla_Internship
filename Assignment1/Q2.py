str1 = "python"
str2 = "programming"
Con = str1 + " " + str2
print(" Concatenated string: ",Con)
print("lower():", Con.lower())
print("upper():", Con.upper())
print("title():", Con.title())
print("swapcase():", Con.swapcase())
print("capitalize():", Con.capitalize())
print("casefold():", Con.casefold())
print("center(50):", Con.center(50, '*'))
print("count('a'):", Con.count('a'))
print("endswith('!'):", Con.endswith('!'))
print("find('a'):", Con.find('a'))
print("isalnum():", Con.isalnum())
print("isdigit():", Con.isdigit())
print("isnumeric():", Con.isnumeric())
print("isspace():", Con.isspace())
print("replace('a','@'):", Con.replace('a', '@'))
